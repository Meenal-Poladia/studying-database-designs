/*

    Primary Key and Alternate Key

    When we want to choose a candidate key to be a primary key we look for the following features:
        1. Key should be unique
        2. Key should never change
        3. Key should never be null

    Primary key is a source of Index.

    Alternate keys are all the candidate keys that were not selected as primary keys. Alternate keys are not must but
    it is good to have them as they can help in searches, or we can create an index.

 */