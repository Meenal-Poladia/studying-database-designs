/*

    Not Null Foreign keys

    Not Null means a every single row has to have a value. Primary keys are required all the time, they are never
    changing and never null.

    Foreign keys values can change as references can change but primary keys remain the same always.
    For example a User at first owns a toyota but later sells the current car and buys Honda car. Here the referencing
    to the car changed but the primary table consisting of the car details remain the same.

    Foreign key values can be null but primary key can never be null. For example - A user can sell ones car and not
    own a car at all.

 */